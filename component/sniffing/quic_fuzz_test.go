package sniffing

import (
	"testing"
)

func FuzzIsLikelyQuicInitialPacket(f *testing.F) {
	f.Add([]byte{0xc0, 0x00, 0x00, 0x00, 0x01, 0x08, 0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08, 0x00, 0x44})
	f.Add([]byte{0xce, 0x00, 0x00, 0x00, 0x01})
	f.Add([]byte{})
	f.Add([]byte{0x40})
	f.Add([]byte{0xd0, 0x00, 0x00, 0x00, 0x01})
	f.Add(make([]byte, 1200))
	f.Add([]byte{0xc0, 0xff, 0x00, 0x00, 0x1d})

	f.Fuzz(func(t *testing.T, data []byte) {
		_ = IsLikelyQuicInitialPacket(data)
	})
}

func FuzzSniffer_SniffUdp_Quic(f *testing.F) {
	f.Add([]byte{0xc0, 0x00, 0x00, 0x00, 0x01, 0x08, 0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08, 0x00, 0x44, 0xd0})
	f.Add([]byte{})
	f.Add([]byte{0xc0})
	f.Add(make([]byte, 64))
	f.Add([]byte{0x40, 0x00, 0x00, 0x00, 0x01, 0x00})

	f.Fuzz(func(t *testing.T, data []byte) {
		if len(data) == 0 {
			return
		}
		sniffer := NewPacketSniffer(data, 0)
		_, _ = sniffer.SniffUdp()
	})
}
