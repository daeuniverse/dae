package sniffing

import (
	"testing"

	"github.com/daeuniverse/dae/component/sniffing/internal/quicutils"
)

func FuzzExtractSniFromTls(f *testing.F) {
	f.Add([]byte{
		0x16, 0x03, 0x01, 0x00, 0x01, 0xfc, 0x03, 0x03,
		0xd9, 0x0f, 0xdf, 0x25, 0xb0, 0xc7, 0xa1, 0x1c,
		0x00, 0x00, 0x00, 0x13, 0x01, 0x00, 0x00, 0x0e,
		0x00, 0x11, 0x00, 0x00, 0x0e, 0x77, 0x77, 0x77,
		0x2e, 0x67, 0x6f, 0x6f, 0x67, 0x6c, 0x65, 0x2e,
		0x63, 0x6f, 0x6d,
	})
	f.Add([]byte{})
	f.Add([]byte{0x16, 0x03, 0x01})
	f.Add([]byte{0x16})
	f.Add(make([]byte, 512))
	f.Add([]byte{
		0x16, 0x03, 0x01, 0x00, 0x05, 0x01, 0x00, 0x00,
		0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	})

	f.Fuzz(func(t *testing.T, data []byte) {
		loc := quicutils.BuiltinBytesLocator(data)
		_, _ = extractSniFromTls(&loc)
	})
}
